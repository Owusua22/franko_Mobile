const generateSitemap = () => {
  const timestamp = new Date().toISOString();

  let sitemap = `<?xml version="1.0" encoding="UTF-8"?>\n`;
  sitemap += `<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">\n`;

  pages.forEach(({ loc, changefreq, priority }) => {
    sitemap += `  <url>\n`;
    sitemap += `    <loc>${loc}</loc>\n`;
    sitemap += `    <lastmod>${timestamp}</lastmod>\n`;
    sitemap += `    <changefreq>${changefreq}</changefreq>\n`;
    sitemap += `    <priority>${priority}</priority>\n`;
    sitemap += `  </url>\n`;
  });

  sitemap += `</urlset>\n`;

  const filePath = join(__dirname, "public", "sitemap.xml");
  writeFileSync(filePath, sitemap.trim() + "\n", "utf8");

  console.log(`âœ… Sitemap generated at ${filePath}`);
};

generateSitemap();
